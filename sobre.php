          <div class="principal texto2" style="margin-top: 2%;">
            <p class="recuo">﻿Como já dito antes, a Rede IRChelp surgiu com o intuído de ajudar àquelas que gostariam de saber
            mais informações sobre o IRC, desde simples comandos até a criação de uma rede. Quando eu mesmo tive essas dúvidas, não encontrei
            ninguém que me ajudasse, ou orientasse a como conseguir essas informações ou ainda a realizar o que eu queria fazer.</p>
            <p class="recuo">Comecei a acessar o IRC de 1995 até final de 1998. Depois voltei a usar em 2016. Já em 95 era muito difícil alguém lhe
            “dar” alguma informação no IRC, pedi ajuda era como um insulto. Hoje possuímos mecanismo de busca melhorados, naquela época
            tínhamos o <a href="https://www.techtudo.com.br/listas/noticia/2016/03/alem-do-google-cade-aonde-altavista-e-outros-fizeram-historia.html" target="_blank" rel="noopener noreferrer">Cadê, Altavista</a> etc, e mesmo assim não
            existia muita literatura, e as que existiam eram em inglês. Tendo em vista tudo isso, foi que surgiu a ideia de criação de uma
            rede de ajuda. Depois veio a ideia de criar o site.</p>
            <p class="recuo">Após ter a ideia da Rede e posteriormente a do site, ao pesquisar os domínios irchelp, descobri que alguém já havia tido essa
            mesma ideia em 1993. A IRChelp original foi concebido como um canal de ajuda também sobre o IRC nas
            redes <a href="http://www.efnet.org/" target="_blank" rel="noopener noreferrer">EFnet</a>, <a href="https://freenode.net/" target="_blank" rel="noopener noreferrer">Freenode</a> e <a href="http://www.ircnet.org/" target="_blank" rel="noopener noreferrer">IRCnet</a>. Como eles mesmo
            informam no site – <a href="http://www.irchelp.org" target="_blank" rel="noopener noreferrer">IRChelp.org</a> - , são um grupo de
            pouco mais de 20 pessoas que mantém o serviço e o site até hoje com o mesmo objetivo.</p>
            <p class="recuo">Quem esta na “estrada” do IRC a muito tempo, sabe que encontrar ajuda não é fácil, ficamos felizes em saber que existem outras
            pessoas que dispõe de seu tempo para ajudar – como hobby – outras pessoas a desmistificar alguns assuntos relacionados ao IRC.
            Esperamos evoluir no nosso trabalho e contribuir com quem queira. Abraços.
